class MultiAgentHub:
    def __init__(self):
        openai_api_key = os.getenv("OPENAI_API_KEY")
        if not openai_api_key:
            raise ValueError("OPENAI_API_KEY environment variable is required!")
        
        self.research_agent = SimpleResearchAgent(openai_api_key)
        self.writer_agent = SimpleWriterAgent(openai_api_key)
        self.reviewer_agent = SimpleReviewerAgent(openai_api_key)
        
        self.status_updates = queue.Queue()
        self.is_processing = False
    
    def update_status(self, agent_name: str, status: str, message: str):
        timestamp = datetime.now().strftime("%H:%M:%S")
        update = {
            "timestamp": timestamp,
            "agent": agent_name,
            "status": status,
            "message": message
        }
        self.status_updates.put(update)
        return update
    
    async def process_collaborative_task(self, task_description: str, requirements: str, 
                                       research_depth: int, writing_style: str, review_strictness: int):
        self.is_processing = True
        results = []
        
        try:
            # Research phase
            self.update_status("ğŸ” Research Agent", "ğŸš€ Working", "Analyzing topic and gathering information...")
            research_data = await self.research_agent.research(task_description)
            
            if "âŒ" in research_data:
                raise Exception("Research phase failed")
            
            results.append(research_data)
            self.update_status("ğŸ” Research Agent", "âœ… Complete", "Research completed successfully!")
            
            # Writing phase  
            self.update_status("âœï¸ Writer Agent", "ğŸš€ Working", f"Creating {writing_style.lower()} content...")
            written_content = await self.writer_agent.write(research_data, requirements, writing_style)
            
            if "âŒ" in written_content:
                raise Exception("Writing phase failed")
            
            results.append(written_content)
            self.update_status("âœï¸ Writer Agent", "âœ… Complete", "Content creation completed!")
            
            # Review phase
            self.update_status("ğŸ“ Reviewer Agent", "ğŸš€ Working", f"Reviewing content (strictness: {review_strictness}/10)...")
            reviewed_content = await self.reviewer_agent.review(written_content, review_strictness)
            
            if "âŒ" in reviewed_content:
                raise Exception("Review phase failed")
            
            results.append(reviewed_content)
            self.update_status("ğŸ“ Reviewer Agent", "âœ… Complete", "Quality review completed!")
            
            self.update_status("ğŸ¤– System", "ğŸ‰ Success", "All AI agents completed successfully!")
            
        except Exception as e:
            error_msg = str(e)
            self.update_status("âŒ System", "ğŸš¨ Error", f"Task failed: {error_msg}")
            results.append(f"âŒ **Error**: {error_msg}")
        finally:
            self.is_processing = False
        
        return self.compile_results(results, task_description)
    
    def compile_results(self, results: list, task: str):
        if not results:
            return "No results available."
        
        final_result = f"# ğŸ¤– Multi-Agent AI Collaboration Results\n\n"
        final_result += f"**Task**: {task}\n"
        final_result += f"**Completed**: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n"
        final_result += f"**Agents**: Research â†’ Writing â†’ Review\n\n"
        final_result += "---\n\n"
        
        for result in results:
            final_result += f"{result}\n\n---\n\n"
        
        if len(results) >= 3:
            final_result += "## ğŸ¯ Collaboration Summary\n\n"
            final_result += "âœ¨ This report demonstrates real AI agents collaborating using OpenAI GPT models.\n"
            final_result += "ğŸ”„ The multi-agent workflow ensures comprehensive coverage and quality output.\n"
        
        return final_result
    
    def get_agent_status(self):
        statuses = []
        while not self.status_updates.empty():
            try:
                statuses.append(self.status_updates.get_nowait())
            except queue.Empty:
                break
        
        if not statuses and not self.is_processing:
            return [{"timestamp": datetime.now().strftime("%H:%M:%S"), 
                    "agent": "System", "status": "ğŸ’¤ Ready", "message": "Waiting for tasks"}]
        
        return statuses[-6:]  # Last 6 updates

# Global hub instance
hub = None

def run_collaborative_task(task_description, requirements, research_depth, writing_style, review_strictness, progress=gr.Progress()):
    if not task_description.strip():
        return "âŒ Please provide a task description.", format_status_display()
    
    if not os.getenv("OPENAI_API_KEY"):
        return "âŒ OpenAI API key not found. Please set OPENAI_API_KEY environment variable.", format_status_display()
    
    loop = asyncio.new_event_loop()
    asyncio.set_event_loop(loop)
    
    try:
        progress(0.1, desc="ğŸš€ Initializing AI agents...")
        progress(0.3, desc="ğŸ” Research Agent working...")
        progress(0.6, desc="âœï¸ Writer Agent creating...")
        progress(0.9, desc="ğŸ“ Reviewer Agent analyzing...")
        
        result = loop.run_until_complete(
            hub.process_collaborative_task(task_description, requirements, research_depth, writing_style, review_strictness)
        )
        
        progress(1.0, desc="ğŸ‰ Collaboration complete!")
        return result, format_status_display()
        
    except Exception as e:
        return f"âŒ Error: {str(e)}", format_status_display()
    finally:
        loop.close()

def format_status_display():
    statuses = hub.get_agent_status()
    if not statuses:
        return "ğŸ’¤ **Status**: Ready for AI collaboration tasks!"
    
    display = "## ğŸ¤– AI Agent Status\n\n"
    for status in statuses:
        emoji = "âœ…" if "âœ…" in status['status'] else "ğŸš€" if "ğŸš€" in status['status'] else "âŒ" if "âŒ" in status['status'] else "â³"
        display += f"{emoji} **{status['timestamp']}** | **{status['agent']}**: {status['message']}\n\n"
    
    return display

def refresh_status():
    return format_status_display()

